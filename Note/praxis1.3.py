## 问题:3.Python中的注释是什么?如何表示注释?注释的作用是什么?
## -------------------------------------------------------------
## 答:Python中的注释是井号，以"#"符号表示，该行"#"符号后的任何内容都是注释
#      注释的作用：解释代码要实现什么目的及其背后的原理
#               让我们改变传统的编程态度：我们的主要任务，不是指导计算机做什么；而是要向人们解释，
#           我们想要计算机做什么
#
# 好的注释有以下两点
# 1.“为什么”哲学：“好的注释是不是重复代码或解释代码。它们阐明作者的意图。注释在更高的抽象水平上解释代码，指出代码想要做什么”
# 2.“怎么样”哲学：如果代码包含新颖的、值得关注的解决方案，请添加注释来解释这种方法。  